"""
Ejercicio 4. Pedir dos números al usuario y hacer todas las
operaciones básicas de una calculadora y mostrarlo por pantalla.
"""

numero1 = int(input("Digite primer número: "))
numero2 = int(input("Digite segundo número 2: "))

print("#### CALCULADORA ####")
print("Suma:" + str(numero1+numero2))
print("Resta:" + str(numero1-numero2))
print("Multiplicación:" + str(numero1*numero2))
print("Division:" + str(numero1/numero2))